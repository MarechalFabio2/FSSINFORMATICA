    /**
     * calcula o valor total a pagar e troco
     */
    private void calcularPagamento() {

        double juros, receber, troco, total, valorDivida;
        try {
            valorDivida = Double.parseDouble(jtfValorDivida.getText());
        } catch (Exception e) {
            valorDivida = 0;
        }
        try {
            juros = Double.parseDouble(jtfJuros.getText());
        } catch (Exception e) {
            juros = 0;
            jtfJuros.setText("0");
        }
        try {
            receber = Double.parseDouble(jtfReceber.getText());
        } catch (Exception e) {
            receber = 0;
        }

        //calcular valor total
        total = valorDivida + juros;
        jtfCobrar.setText(bLMascaras.arredondamentoComPontoDuasCasasString(total));
        //calcular troco
        troco = receber - total;
        jtfTroco.setText(bLMascaras.arredondamentoComPontoDuasCasasString(troco));
    }

