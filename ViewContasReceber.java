    private void salvarConta() throws Exception {
        try {
            modelContasReceber.setIdContasReceber(Integer.parseInt(this.jtfNumeroConta.getText()));
        } catch (NumberFormatException e) {
        }
        modelContasReceber.setFkCliente(Integer.parseInt(this.jtfCodigoCliente.getText()));
        modelContasReceber.setFkPagamento(Integer.parseInt(this.jtfCodigoPagamento.getText()));
        modelContasReceber.setDescricaoContasReceber(this.jtfDescricao.getText());
        try {
            modelContasReceber.setDataContasReceber(bLDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
            modelContasReceber.setVencimentoContasReceber(bLDatas.converterDataParaDateUS(this.jtfVencimento.getDate()));
            modelContasReceber.setPagamentoContasReceber(bLDatas.converterDataParaDateUS(this.jtfPagamento.getDate()));
        } catch (Exception e) {
        }
        modelContasReceber.setValorContasReceber(formatador.converterVirgulaParaPontoReturn(this.jtfValor.getText()));
        modelContasReceber.setObservacaoContasReceber(this.jtaObservacao.getText());

        if (controllerContasReceber.salvarContasReceberController(modelContasReceber)<0) {
            JOptionPane.showMessageDialog(this, "Conta salva com sucesso", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.limpaFormulario();
            this.habilitaDesabilitaCampos(false);
            this.carregarContasReceber();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar conta", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void alterarConta() {
        try {
            modelContasReceber.setIdContasReceber(Integer.parseInt(this.jtfNumeroConta.getText()));
        } catch (NumberFormatException e) {
        }
        modelContasReceber.setFkCliente(Integer.parseInt(this.jtfCodigoCliente.getText()));
        modelContasReceber.setFkPagamento(Integer.parseInt(this.jtfCodigoPagamento.getText()));
        modelContasReceber.setDescricaoContasReceber(this.jtfDescricao.getText());
        try {
            modelContasReceber.setDataContasReceber(bLDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
            modelContasReceber.setVencimentoContasReceber(bLDatas.converterDataParaDateUS(this.jtfVencimento.getDate()));
            modelContasReceber.setPagamentoContasReceber(bLDatas.converterDataParaDateUS(this.jtfPagamento.getDate()));
        } catch (Exception e) {
        }
        modelContasReceber.setValorContasReceber(formatador.converterVirgulaParaPontoReturn(this.jtfValor.getText()));
        modelContasReceber.setObservacaoContasReceber(this.jtaObservacao.getText());

        if (controllerContasReceber.atualizarContasReceberController(modelContasReceber)) {
            JOptionPane.showMessageDialog(this, "Conta alterada com sucesso", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.limpaFormulario();
            this.habilitaDesabilitaCampos(false);
            this.carregarContasReceber();
            jTabbedPane1.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar conta", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

